<?php

class CountryCuisine extends Chef
{
    private $country;

    /**
     * @param $country
     */
    public function __construct($country)
    {
        $this->country = $country;
    }

    /**
     * @return mixed
     */
    public function getCountry()
    {
        return $this->country;
    }

    /**
     * @param mixed $country
     */
    public function setCountry($country): void
    {
        $this->country = $country;
    }

}